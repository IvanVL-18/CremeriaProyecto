    public void mostrar(){
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = Data.tabla("select Nombre, Precio, Cantidad, Marca from ventas");
        modelo.setColumnIdentifiers(new Object[]{"Nombre:", "Precio:", "Cantidad", "Marca:"});
        try{
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("Nombre"), rs.getString("Precio"), rs.getString("Cantidad"), rs.getString("Marca")});
            }
            jTable1.setModel(modelo);
        }catch(Exception e){
            
        }
    }
    
