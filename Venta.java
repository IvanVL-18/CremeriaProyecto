    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            conectar();
            PreparedStatement pst = cn.prepareStatement("select* from ventas where Nombre = ?");
            
            pst.setString(1, text_buscar.getText().trim());
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                text_nom.setText(rs.getString("Nombre"));
                text_pre.setText(rs.getString("Precio"));
                text_cant.setText(rs.getString("Cantidad"));
                text_marca.setText(rs.getString("Marca"));
            }else{
                JOptionPane.showMessageDialog(null, "Producto no encontrado:(");
            }
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

